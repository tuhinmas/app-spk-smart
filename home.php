<div class="entry-content">
    <p>Pada hakekatnya Simple Multy Attribute Rating (SMART) merupakan suatu model pengambil keputusan yang komprehensif
        dengan memperhitungkan hal-hal yang bersifat kualitatif dan kuantitatif. Dalam model pengambilan keputusan
        dengan SMART pada dasarnya berusaha menutupi setiap kekurangan dari model-model tanpa komputerisasi sebelumnya.
        SMART juga memungkinkan ke struktur suatu sistem dan lingkungan kedalam komponen saling berinteraksi dan
        kemudian menyatukan mereka dengan mengukur dan mengatur dampak dari komponen kesalahan sistem.</p>
    <p>Peralatan utama dari model ini adalah sebuah hirarki fungsional dengan input utamanya adalah persepsi manusia.
        Jadi pada dasarnya tidak ada perbedaan yang mencolok pada model SMART dengan model lainnya sama sama terletak
        pada jenis inputnya, hanya saja terdapat persentase dari setiap pembobotan langsung yang ditentukan oleh hasil
        analisa permasalahan.</p>
